class Solution
{
    public:
    //Function to count number of ways to reach the nth stair.
    int countWays(int n)
    {
        // your code here
        int step1=1,step2=1,mod=1e9+7;
        for(int i=2;i<=n;i++)
        {
            int a=(step1+step2)%mod;
            step2=step1;
            step1=a;
        }
        return step1;
    }
};



