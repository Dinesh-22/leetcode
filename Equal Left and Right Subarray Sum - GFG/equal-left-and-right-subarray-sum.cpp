class Solution {
  public:
    int equalSum(int N, vector<int> &A) {
        // code here
        int leftSum = 0 , total = 0 , rightSum = 0;
        if(N==1) return 1;
        for(int i=0;i<N;i++){
            total+=A[i];
        }
        for(int i=0;i<N;i++){
            if(i==0) continue;
            leftSum+=A[i-1];
            rightSum = total - leftSum - A[i];
            if(leftSum == rightSum){
                return i+1;
            }
        }
        return -1;
    }
};


